//Las funciones son bloques de código reutilizables que se ejecutan cuando las llamas.

// Maneras de escribir una funcion

//Declaración de función (Function Declaration)    Forma tradicional.

function saludar(nombre) {
  return "Hola " + nombre;
}

//Expresión de función (Function Expression)
//Se guarda en una variable.

const saludar = function(nombre) {
  return "Hola " + nombre;
};

